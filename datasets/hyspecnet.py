class HyspecnetDataset(Dataset):

    def __init__(self, root_dir, harmonizer):
        self.files = [
            os.path.join(root_dir, f)
            for f in os.listdir(root_dir)
            if f.endswith(".npy")
        ]

        self.harmonizer = harmonizer

    def __len__(self):
        return len(self.files)

    def __getitem__(self, idx):

        patch = np.load(self.files[idx])
        # remove overlapped bands between vnir and swir
        patch = np.delete(patch, np.arange(91, 101), axis=0)
        pixels = patch.reshape(patch.shape[0], -1)

        # apply SG smoothing
        smoothed_pixels = savgol_filter(
            pixels,
            window_length=5,
            polyorder=2,
            axis=0
        )

        # apply spectral harmonizer
        harmonized_pixels = self.harmonizer.harmonize(smoothed_pixels)
        return torch.tensor(harmonized_pixels, dtype=torch.float32)